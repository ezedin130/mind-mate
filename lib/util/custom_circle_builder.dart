
import 'package:flutter/material.dart';

class CustomCircleBuilder extends StatelessWidget {
  const CustomCircleBuilder({
    super.key,
    required this.size,
  });

  final double size;

  @override
  Widget build(BuildContext context) {
    return Container(
      width: size,
      height: size,
      decoration: const BoxDecoration(
        color: Colors.white,
        shape: BoxShape.circle,
      ),
    );
  }
}